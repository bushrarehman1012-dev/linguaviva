function getWordListContext(langCode, text) {
  const wordList = WORD_LISTS[langCode];
  if (!wordList) return '';
  const lowerText = text.toLowerCase().trim();

  // Check for exact whole-input phrase match first (highest priority)
  const exactPhrase = wordList[lowerText];
  if (exactPhrase) {
    return `\nVERIFIED PHRASE (use this exact transliteration):\n"${lowerText}" = "${exactPhrase}"\n`;
  }

  // Sort by length descending so longer phrase matches beat shorter word matches
  const matches = Object.entries(wordList)
    .filter(([eng]) => lowerText.includes(eng))
    .sort((a, b) => b[0].length - a[0].length)
    .map(([eng, target]) => `"${eng}" = "${target}"`);

  if (matches.length === 0) return '';
  return `\nVerified vocabulary from linguistic sources (use these exact forms):\n${matches.join('\n')}\n`;
}

